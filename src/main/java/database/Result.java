    // Calculate Rank
    private static ArrayList<DataTable> dataListBean(ArrayList<DataList> dataList) throws Exception
    {
        Facade facade = new Facade();

        ArrayList<DataTable> dataTable  = new ArrayList<>();

        ArrayList<String[]> dataLists = facade.calculateRanking(dataList, 5);//ArrayList<String[]> dataLists = facade.calculateRanking(dataList, dataList.size());
        for(String[] s : dataLists)
        {
            DataTable objData = new DataTable();
            objData.setTest(s[0]);
            objData.setModel(s[1]);
            objData.setWeight(s[2]);
            dataTable.add(objData);
        }
        facade.close();

        return dataTable;
    }

